int accept_object(object who, object ob)
{
        object          shu;
        if( ob->name() == "铁剑") {
                say("大汉看了你一眼，说道：谢谢。\n");
                if( shu = present("shu zhi", this_object()) ) {
                        say("大汉接过铁剑，顺手把树枝丢在地上。\n");
                        shu->move(environment(this_object()));
                }
                return 1;
        }
        return 0;
}        
